//! Adapter that serves an existing tddy-rpc `ConnectionService` implementation
//! (e.g. [`crate::connection_service::ConnectionServiceImpl`]) over native tonic gRPC.
//!
//! The daemon's handler is written against the tddy-rpc flavor of the service trait
//! (`tddy_service::proto::connection::ConnectionService`, using `tddy_rpc::{Request, Response,
//! Status, Streaming}`). This adapter implements the tonic flavor
//! (`tddy_service::tonic_connection::connection_service_server::ConnectionService`, using
//! `tonic::{Request, Response, Status, Streaming}`) by delegating each method to the inner
//! handler and converting the wrapper types at the boundary.
//!
//! Both flavors reference the identical canonical prost message types (via `extern_path` in
//! `tddy-service/build.rs`), so no message re-encode/decode is needed — only the transport
//! wrappers and the error `Status` are converted. The tddy-rpc `tonic` feature provides `From`
//! impls for these conversions, but it pins tonic 0.11 while the generated tonic service targets
//! tonic 0.12; the conversions here are therefore written explicitly against the 0.12 types.
//!
//! Each method is spelled out as a literal `async fn` (rather than generated by a declarative
//! macro) because `#[tonic::async_trait]` rewrites method signatures and cannot see through a
//! macro invocation.
//!
//! This is plumbing only: constructing the adapter does not start a server. Wiring a
//! `tonic::transport::Server` around `ConnectionServiceServer::new(adapter)` is a later step.

