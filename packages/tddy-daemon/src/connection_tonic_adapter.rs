/// Maps a peer uid (from SO_PEERCRED) to its OS username. Injected so the local peer-trust path is
/// host-independent and unit-testable; production wires the passwd lookup
/// (`crate::user_sessions_path::username_for_uid`).
pub type UidToUsername = Arc<dyn Fn(u32) -> Option<String> + Send + Sync>;

/// Wraps a tddy-rpc `ConnectionService` implementation so it can be served over tonic gRPC.
///
/// Construct with [`ConnectionServiceTonicAdapter::new`] and hand to
/// `tddy_service::tonic_connection::connection_service_server::ConnectionServiceServer::new`.
///
/// Every method delegates to the inner tddy-rpc handler except `MintLocalToken`, which is handled
/// here at the tonic layer: it reads the caller's peer uid from the Unix-domain-socket connect-info
/// (`UdsConnectInfo`, populated by tonic on the local socket transport), resolves it against the
/// daemon `users[]` mapping, and mints an access token with the shared `SessionTokenSigner`.
pub struct ConnectionServiceTonicAdapter<T> {
    inner: Arc<T>,
    config: Arc<DaemonConfig>,
    signer: Option<SessionTokenSigner>,
    uid_to_username: UidToUsername,
}

impl<T> ConnectionServiceTonicAdapter<T> {
    pub fn new(
        inner: Arc<T>,
        config: Arc<DaemonConfig>,
        signer: Option<SessionTokenSigner>,
        uid_to_username: UidToUsername,
    ) -> Self {
        Self {
            inner,
            config,
            signer,
            uid_to_username,
        }
    }
}

/// Convert a tddy-rpc `Status` into a tonic `Status` (tonic 0.12).
fn to_tonic_status(status: tddy_rpc::Status) -> tonic::Status {
    let code = match status.code() {
        tddy_rpc::Code::Ok => tonic::Code::Ok,
        tddy_rpc::Code::Cancelled => tonic::Code::Cancelled,
        tddy_rpc::Code::Unknown => tonic::Code::Unknown,
        tddy_rpc::Code::InvalidArgument => tonic::Code::InvalidArgument,
        tddy_rpc::Code::DeadlineExceeded => tonic::Code::DeadlineExceeded,
        tddy_rpc::Code::NotFound => tonic::Code::NotFound,
        tddy_rpc::Code::AlreadyExists => tonic::Code::AlreadyExists,
        tddy_rpc::Code::PermissionDenied => tonic::Code::PermissionDenied,
        tddy_rpc::Code::ResourceExhausted => tonic::Code::ResourceExhausted,
        tddy_rpc::Code::FailedPrecondition => tonic::Code::FailedPrecondition,
        tddy_rpc::Code::Aborted => tonic::Code::Aborted,
        tddy_rpc::Code::OutOfRange => tonic::Code::OutOfRange,
        tddy_rpc::Code::Unimplemented => tonic::Code::Unimplemented,
        tddy_rpc::Code::Internal => tonic::Code::Internal,
        tddy_rpc::Code::Unavailable => tonic::Code::Unavailable,
        tddy_rpc::Code::DataLoss => tonic::Code::DataLoss,
        tddy_rpc::Code::Unauthenticated => tonic::Code::Unauthenticated,
    };
    tonic::Status::new(code, status.message().to_string())
}

/// Convert a tonic `Status` (tonic 0.12) into a tddy-rpc `Status`.
